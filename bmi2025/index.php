    <h1 class="">BMI introduction</h1>
    <p class="solid"><abbr title="Body mass Index">BMI</abbr> is a measurement of a person's leanness or corpulence based on their height and weight, and is intended to quantify tissue mass. It is widely used as a general indicator of whether a person has a healthy body weight for their height. Specifically, the value obtained from the calculation of <b>BMI</b> is used to categorize whether a person is <mark>underweight</mark>, <mark>normal weight</mark>, <mark>overweight</mark>, or <mark>obese</mark> depending on what range the value falls between.
    </p>

